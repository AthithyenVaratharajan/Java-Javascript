public class Student {
    String firstName;
    String lastName;
    double gpa;
    String major;
    int age;
    boolean onProbation;

    public Student(String firstName, String lastName, double gpa, String major, int age, boolean onProbation){
        this.firstName = firstName;
        this.lastName = lastName;
        this.gpa = gpa;
        this.major = major;
        this.age = age;
        this.onProbation = onProbation;
    }
}
